package recordManagement;

public class LabelRecord {
	private String name;
	private int index;
	
	public LabelRecord(String newName, int newIndex) {
		setName(newName);
		setIndex(newIndex);
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the index
	 */
	public int getIndex() {
		return index;
	}

	/**
	 * @param index the index to set
	 */
	public void setIndex(int index) {
		this.index = index;
	}

}
